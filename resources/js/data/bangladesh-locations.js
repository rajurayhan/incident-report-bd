export const getDivisions = () => {
  return Object.keys(bangladeshLocations);
};

export const getDistricts = (division) => {
  if (!division || !bangladeshLocations[division]) return [];
  return Object.keys(bangladeshLocations[division].districts);
};

export const getThanas = (division, district) => {
  if (!division || !district || !bangladeshLocations[division]) return [];
  const districts = bangladeshLocations[division].districts;
  if (!districts[district]) return [];
  return districts[district];
};

